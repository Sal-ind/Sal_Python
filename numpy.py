# Creation of Arrays
import numpy as np
 
# Creating a rank 1 Array
arr = np.array([1, 2, 3])
print(" \n",arr)
 
# Two diamensional array
arr = np.array([[1, 2],
                [3,4]])
print(" \n", arr)
 
# three dimensional array
arr = np.array([[1, 2, 3],[4,5,6],[8,9,1],[0,3,4]])
print("\nArray created using "
      "passed tuple:\n", arr)